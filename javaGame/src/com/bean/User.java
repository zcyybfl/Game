public class User {

    private int id;
    //用户名
    private String username;
    //密码
    private String password;

    //贪吃蛇分数
    private int SnakeScore;

    //飞机大战分数
    private int PlaneScore;

    public User() {

    }

    public User(String username, String password) {
        this.username = username;
        this.password = password;
    }

    public int getPlaneScore() {
        return PlaneScore;
    }

    public void setPlaneScore(int planeScore) {
        PlaneScore = planeScore;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public int getSnakeScore() {
        return SnakeScore;
    }

    public void setSnakeScore(int snakeScore) {
        SnakeScore = snakeScore;
    }
}
